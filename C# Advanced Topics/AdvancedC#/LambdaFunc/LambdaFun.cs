namespace AdvancedC_.LambdaFunc;

public class LambdaFunc
{
    /*
     * Lambda Functions:
     * Demonstrate the usage of lambda functions using a function
     * that accepts a number and returns a square of that number.
     * Part 1: Using standard function notation.
     * Part 2: See calling (Main) function.
     */
    public int Square(int num)
    {
        return (num * num);
    }
}